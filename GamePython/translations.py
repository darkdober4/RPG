"""Système de traduction FR → EN pour GamePython RPG"""

_current_language = "fr"


def set_language(lang):
    global _current_language
    _current_language = lang


def get_language():
    return _current_language


def t(text, lang=None):
    """Traduit un texte français en anglais si lang='en'."""
    lang = lang or _current_language
    if lang == "en":
        return TRANSLATIONS.get(text, text)
    return text


